a = int(input("Inputkan nilai a: "))
b = int(input("Inputkan nilai b: "))

# Penjumlahan
c = a + b
print("Hasil %d + %d = %d" % (a,b,c))

# Pengurangan
c = a - b
print("Hasil %d - %d = %d" % (a,b,c))

# Pembagian
c = a / b
print("Hasil %d / %d = %d" % (a,b,c))

# perpangkatan
c = a ** 2
print("Hasil %d ** 2 = %d" % (a,c))
